//Write a program in java to demonstrate usage of Primitive Data types â€“ 
//Boolean, char, byte, short, int, long, float and double

public class PrimitiveDataTypesDemo {
    public static void main(String[] args) {
        // boolean
        boolean isJavaFun = true;
        System.out.println("Is Java fun? " + isJavaFun);

        // char
        char grade = 'A';
        System.out.println("Your grade is: " + grade);

        // byte
        byte smallNumber = 10;
        System.out.println("A small number: " + smallNumber);

        // short
        short smallInteger = 1000;
        System.out.println("A small integer: " + smallInteger);

        // int
        int age = 25;
        System.out.println("Your age is: " + age);

        // long
        long bigNumber = 1234567890L; 
        System.out.println("A big number: " + bigNumber);

        // float
        float piApproximation = 3.14159f; 
        System.out.println("Approximate value of pi: " + piApproximation);

        // double
        double pi = 3.141592653589793;
        System.out.println("Accurate value of pi: " + pi);
    }
}
